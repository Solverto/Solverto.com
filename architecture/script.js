function renderFilters() {
  const filterLabels = t("filters");
  document.querySelector("[data-project-filters]").innerHTML = Object.entries(filterLabels)
    .map(([id, label]) => `<button type="button" data-project-filter="${id}" class="${activeFilter === id ? "is-active" : ""}">${label}</button>`)
    .join("");
}

function renderProjects() {
  const query = normalize(searchValue);
  const projects = architectureProjects.filter((project) => {
    const matchesFilter = activeFilter === "all" || project.country === activeFilter || (activeFilter === "largest" && project.largest);
    const matchesSearch = normalize(`${project.title} ${project.client} ${t(`filters.${project.country}`)}`).includes(query);
    return matchesFilter && matchesSearch;
  });

  document.querySelector("[data-project-count]").textContent = `${projects.length} ${t("projects.count")}`;
  document.querySelector("[data-architecture-projects]").innerHTML = projects.length
    ? projects.map((project) => `
      <article class="architecture-project-card reveal">
        <div class="project-meta">
          <span class="badge">${t(`filters.${project.country}`)}</span>
          ${project.largest ? `<span class="badge largest">${t("filters.largest")}</span>` : ""}
        </div>
        <h3>${project.title}</h3>
        <p class="architecture-client">${project.client}</p>
        <p>${t("projects.service")}</p>
      </article>
    `).join("")
    : `<p class="notice">${t("projects.empty")}</p>`;
}

